#ifndef M_PI
#define M_PI 3.14159265358979323846
#endif

static const double MS_TO_KTS = 1.94384;
static const double DEG_TO_RAD = M_PI / 180.0;
static const double METERS_PER_DEG = 111320.0;

// ============================================================================
// 校验
// ============================================================================

bool ScenarioConverter::validateScenario(const QJsonObject& json, QString& errorMsg)
{
    if (json.isEmpty()) {
        errorMsg = "scenario object is empty";
        return false;
    }
    if (!json.contains("vecTeam")) {
        errorMsg = "missing required field: vecTeam";
        return false;
    }
    const QJsonArray teams = json["vecTeam"].toArray();
    if (teams.isEmpty()) {
        errorMsg = "vecTeam is empty, at least one team required";
        return false;
    }
    for (int i = 0; i < teams.size(); ++i) {
        QJsonObject team = teams[i].toObject();
        QJsonObject manner = team["tObjManner"].toObject();
        if (manner.isEmpty()) {
            errorMsg = QString("team[%1] missing tObjManner").arg(i);
            return false;
        }
        QJsonObject status = manner["iObjStatus"].toObject();
        if (status.isEmpty()) {
            errorMsg = QString("team[%1].tObjManner missing iObjStatus").arg(i);
            return false;
        }
        QJsonObject pos = status["iPos"].toObject();
        if (pos.isEmpty()) {
            errorMsg = QString("team[%1].tObjManner.iObjStatus missing iPos").arg(i);
            return false;
        }
    }
    return true;
}

// ============================================================================
// 主转换入口
// ============================================================================

QString ScenarioConverter::convertToAfsimTxt(const QJsonObject& json)
{
    QString out;
    QTextStream ts(&out);

    // ---- 头部注释 ----
    QString name  = json["strScenarioName"].toString("unnamed");
    QString author = json["strAuthor"].toString("unknown");
    QString desc  = json["strDescription"].toString("");

    ts << "# Generated by RASimBackend init_scenario\n";
    ts << "# 想定名称: " << name << "\n";
    ts << "# 制作者: " << author << "\n";
    if (!desc.isEmpty())
        ts << "# 描述: " << desc << "\n";
    ts << "\n";

    ts << "frame_time 0.01 seconds\n\n";

    // ---- 计算仿真时长 ----
    QString strStart = json["strStartTime"].toString();
    QString strEnd   = json["strEndTime"].toString();
    double endSec = calcEndTimeSec(strStart, strEnd);
    if (endSec <= 0) endSec = 1800.0;

    // ---- 收集所有用到的平台类型，生成 platform_type 块 ----
    QSet<int> usedPlatTypes;
    QJsonArray teams = json["vecTeam"].toArray();
    for (const QJsonValue& tv : teams) {
        QJsonObject team = tv.toObject();
        QJsonObject manner = team["tObjManner"].toObject();
        QJsonObject baseInfo = manner["iBaseInfo"].toObject();
        int typeId = baseInfo["nTypeID"].toInt(1);
        usedPlatTypes.insert(typeId);
        // 编队成员也检查
        QJsonArray members = team["vecTeamInfo"].toArray();
        for (const QJsonValue& mv : members) {
            QJsonObject member = mv.toObject();
            QJsonObject mBase = member["iBaseInfo"].toObject();
            int mTypeId = mBase["nTypeID"].toInt(typeId);
            usedPlatTypes.insert(mTypeId);
        }
    }

    ts << "# ---- 平台类型定义 ----\n\n";
    for (int typeId : usedPlatTypes) {
        QString cat = platTypeToCategory(typeId);
        QString mover = platTypeToMover(typeId, false);
        QString icon  = platTypeToIcon(typeId);
        ts << "platform_type RA_" << cat.toUpper() << " WSF_PLATFORM\n";
        ts << "   icon " << icon << "\n";
        ts << "   category " << cat << "\n";
        if (!mover.isEmpty()) {
            ts << "   mover " << mover << "\n";
            ts << "      at_end_of_path extrapolate\n";
            ts << "   end_mover\n";
        }
        ts << "end_platform_type\n\n";
    }

    // ---- 生成平台实例 ----
    ts << "# ---- 平台实例 ----\n\n";
    for (const QJsonValue& tv : teams) {
        QJsonObject team = tv.toObject();
        QJsonObject manner = team["tObjManner"].toObject();
        QJsonObject baseInfo = manner["iBaseInfo"].toObject();
        QJsonObject status   = manner["iObjStatus"].toObject();
        QJsonObject pos      = status["iPos"].toObject();
        QJsonObject hpr      = status["iHpr"].toObject();

        QString teamId = manner["str_id"].toString(team["strTeamID"].toString("unknown"));
        int factionId  = baseInfo["nFactionID"].toInt(1);
        int typeId     = baseInfo["nTypeID"].toInt(1);
        bool isStatic  = manner["bStatic"].toBool(false);

        QString side = factionToSide(factionId);
        QString cat  = platTypeToCategory(typeId);
        QString typeName = QString("RA_%1").arg(cat.toUpper());

        double lon = pos["x"].toDouble(0.0);
        double lat = pos["y"].toDouble(0.0);
        double alt = pos["z"].toDouble(0.0);
        double heading = hpr["x"].toDouble(0.0);
        double speed   = status["dHSpd"].toDouble(0.0);

        // 载荷
        QJsonObject payloads = team["map_load"].toObject();

        ts << genPlatform(teamId, typeName, side, cat,
                          lon, lat, alt, heading, speed,
                          isStatic, endSec, payloads);

        // ---- 编队成员 ----
        QJsonArray members = team["vecTeamInfo"].toArray();
        for (const QJsonValue& mv : members) {
            QJsonObject member = mv.toObject();
            QJsonObject mBase = member["iBaseInfo"].toObject();
            QJsonObject relPos = member["iRelativePos"].toObject();

            QString memberId = member["strObjID"].toString();
            int mFactionId = mBase["nFactionID"].toInt(factionId);
            int mTypeId    = mBase["nTypeID"].toInt(typeId);

            QString mSide = factionToSide(mFactionId);
            QString mCat  = platTypeToCategory(mTypeId);
            QString mTypeName = QString("RA_%1").arg(mCat.toUpper());

            double relDis = relPos["dRelativeDis"].toDouble(0.0);
            double relHdg = relPos["dRelativeHdg"].toDouble(0.0);
            double relAlt = relPos["dRelativeAlt"].toDouble(0.0);

            double mLon, mLat, mAlt;
            calcMemberPosition(lon, lat, alt, heading, relDis, relHdg, relAlt,
                               mLon, mLat, mAlt);

            QJsonObject mPayloads = member["map_load"].toObject();

            ts << genPlatform(memberId, mTypeName, mSide, mCat,
                              mLon, mLat, mAlt, heading, speed,
                              isStatic, endSec, mPayloads);
        }
    }

    // ---- 区域 ----
    QJsonArray areas = json["vecArea"].toArray();
    if (!areas.isEmpty()) {
        ts << "# ---- 区域 ----\n\n";
        for (int i = 0; i < areas.size(); ++i) {
            ts << genZone(QString("area_%1").arg(i), areas[i].toObject());
        }
    }

    // ---- 仿真时长 ----
    ts << "# ---- 仿真时长 ----\n";
    ts << "end_time " << static_cast<int>(endSec) << " sec\n";

    ts.flush();
    return out;
}

