QString ScenarioConverter::formatPosition(double lon, double lat, double altM)
{
    // AFSIM 支持: position 30.37n 120.74e altitude 5000 m
    QString latDir = (lat >= 0) ? "n" : "s";
    QString lonDir = (lon >= 0) ? "e" : "w";
    return QString("position %1%2 %3%4 altitude %5 m")
        .arg(qFabs(lat), 0, 'f', 6)
        .arg(latDir)
        .arg(qFabs(lon), 0, 'f', 6)
        .arg(lonDir)
        .arg(altM, 0, 'f', 1);
}

QString ScenarioConverter::formatSpeed(double speedMs)
{
    double kts = speedMs * MS_TO_KTS;
    return QString::number(qFabs(kts), 'f', 1) + " kts";
}

bool ScenarioConverter::isCoincident(double lon1, double lat1, double alt1,
                                     double lon2, double lat2, double alt2)
{
    static const double POS_EPS = 1e-5;  // ~1.1m
    static const double ALT_EPS = 1.0;   // 1m
    return qFabs(lon1 - lon2) < POS_EPS &&
           qFabs(lat1 - lat2) < POS_EPS &&
           qFabs(alt1 - alt2) < ALT_EPS;
}

// ============================================================================
// 时间计算
// ============================================================================

double ScenarioConverter::calcEndTimeSec(const QString& start, const QString& end)
{
    if (start.isEmpty() || end.isEmpty()) return 0.0;
    QDateTime dtStart = QDateTime::fromString(start, "yyyy-MM-dd hh:mm:ss");
    QDateTime dtEnd   = QDateTime::fromString(end,   "yyyy-MM-dd hh:mm:ss");
    if (!dtStart.isValid() || !dtEnd.isValid()) return 0.0;
    return dtStart.secsTo(dtEnd);
}

