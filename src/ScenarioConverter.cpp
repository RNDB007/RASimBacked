QString ScenarioConverter::factionToSide(int factionId)
{
    switch (factionId) {
    case 1:  return "red";
    case 2:  return "blue";
    case 3:  return "green";
    case 4:  return "yellow";
    case 5:  return "green";
    default: return "red";
    }
}

QString ScenarioConverter::platTypeToCategory(int typeId)
{
    switch (typeId) {
    case 1:     return "fighter";     // TYPE_AIRCRAFT
    case 2:     return "ship";        // TYPE_WARSHIP
    case 4:     return "submarine";   // TYPE_SUBMARINE
    case 8:     return "truck";       // TYPE_VEHICLE
    case 16:    return "satellite";   // TYPE_SATELLITE
    case 32:    return "person";      // TYPE_PERSON
    case 64:    return "mine";        // TYPE_MINE
    case 32768: return "building";    // TYPE_BUILDING
    case 65536: return "landmark";    // TYPE_LANDMARK
    default:    return "platform";
    }
}

QString ScenarioConverter::platTypeToMover(int typeId, bool isStatic)
{
    if (isStatic) return QString();
    switch (typeId) {
    case 1:     return "WSF_AIR_MOVER";
    case 2:     return "WSF_SURFACE_MOVER";
    case 4:     return "WSF_SURFACE_MOVER";
    case 8:     return "WSF_SURFACE_MOVER";
    case 16:    return "WSF_SPACE_MOVER";
    default:    return QString();
    }
}

QString ScenarioConverter::platTypeToIcon(int typeId)
{
    switch (typeId) {
    case 1:     return "f15c";
    case 2:     return "ship";
    case 4:     return "submarine";
    case 8:     return "truck";
    case 16:    return "satellite";
    case 32:    return "person";
    case 64:    return "mine";
    case 32768: return "building";
    case 65536: return "landmark";
    default:    return "platform";
    }
}

QString ScenarioConverter::partTypeToAfsim(int typeId)
{
    // TYPE_RADAR = 1<<20 = 1048576
    if (typeId & (1 << 20)) return "sensor";
    // TYPE_SONAR = 1<<21 = 2097152
    if (typeId & (1 << 21)) return "sensor";
    // TYPE_MISSILE = 1<<18 = 262144
    if (typeId & (1 << 18)) return "weapon";
    // TYPE_JAMMER = 1<<22 = 4194304
    if (typeId & (1 << 22)) return "sensor";  // EW sensor placeholder
    // TYPE_PSE = 1<<26 = 67108864
    if (typeId & (1 << 26)) return "sensor";
    return QString();
}

