QString ScenarioConverter::genPlatformType(const QString& typeName, int typeId, bool isStatic)
{
    QString cat = platTypeToCategory(typeId);
    QString mover = platTypeToMover(typeId, isStatic);
    QString icon  = platTypeToIcon(typeId);

    QString out;
    QTextStream ts(&out);
    ts << "platform_type " << typeName << " WSF_PLATFORM\n";
    ts << "   icon " << icon << "\n";
    ts << "   category " << cat << "\n";
    if (!mover.isEmpty()) {
        ts << "   mover " << mover << "\n";
        ts << "      at_end_of_path extrapolate\n";
        ts << "   end_mover\n";
    }
    ts << "end_platform_type\n\n";
    ts.flush();
    return out;
}

QString ScenarioConverter::genPlatform(const QString& instanceName,
                                       const QString& typeName,
                                       const QString& side,
                                       const QString& category,
                                       double lon, double lat, double altM,
                                       double heading, double speedMs,
                                       bool isStatic,
                                       double endSec,
                                       const QJsonObject& payloads)
{
    QString out;
    QTextStream ts(&out);

    ts << "platform " << instanceName << " " << typeName << "\n";
    ts << "   side " << side << "\n";
    ts << "   category " << category << "\n";
    ts << "   " << formatPosition(lon, lat, altM) << "\n";
    ts << "   heading " << QString::number(heading, 'f', 1) << " deg\n";

    // 活动平台生成简单两点航路
    if (!isStatic && speedMs > 0.1) {
        double kts = speedMs * MS_TO_KTS;
        // 沿航向投影一个点作为航路终点
        double hdgRad = heading * DEG_TO_RAD;
        double distM = speedMs * endSec;
        // 限制最大距离为 500km 以保持合理
        if (distM > 500000.0) distM = 500000.0;
        double deltaLat = -(distM * cos(hdgRad)) / METERS_PER_DEG;
        double deltaLon = (distM * sin(hdgRad)) / (METERS_PER_DEG * cos(lat * DEG_TO_RAD));
        double endLon = lon + deltaLon;
        double endLat = lat + deltaLat;

        ts << "   route\n";
        ts << "      " << formatPosition(lon, lat, altM)
           << " speed " << formatSpeed(speedMs) << "\n";
        ts << "      " << formatPosition(endLon, endLat, altM)
           << " speed " << formatSpeed(speedMs) << "\n";
        ts << "   end_route\n";
    }

    // 载荷
    QString payloadBlock = genPayloads(payloads);
    if (!payloadBlock.isEmpty()) {
        ts << payloadBlock;
    }

    ts << "end_platform\n\n";
    ts.flush();
    return out;
}

QString ScenarioConverter::genPayloads(const QJsonObject& payloads)
{
    if (payloads.isEmpty()) return QString();

    QString out;
    QTextStream ts(&out);

    for (auto it = payloads.begin(); it != payloads.end(); ++it) {
        QJsonObject part = it.value().toObject();
        QJsonObject baseInfo = part["iBaseInfo"].toObject();
        int typeId = baseInfo["nTypeID"].toInt(0);
        QString partId = part["strPartID"].toString(it.key());

        QString afsimType = partTypeToAfsim(typeId);
        if (afsimType == "sensor") {
            ts << "   sensor " << partId << " WSF_SENSOR\n";
            ts << "      on\n";
            ts << "   end_sensor\n";
        } else if (afsimType == "weapon") {
            ts << "   weapon " << partId << " WSF_EXPLICIT_WEAPON\n";
            ts << "      quantity 4\n";
            ts << "   end_weapon\n";
        }
        // 其他类型 v1 暂不处理

        // 递归处理子载荷
        QJsonObject subParts = part["map_load_part"].toObject();
        if (!subParts.isEmpty()) {
            ts << genPayloads(subParts);
        }
    }

    ts.flush();
    return out;
}

QString ScenarioConverter::genZone(const QString& name, const QJsonObject& area)
{
    QString out;
    QTextStream ts(&out);

    int areaType = area["nAreaType"].toInt(0);
    QString message = area["sMessage"].toString(name);
    double upH   = area["dUpHeight"].toDouble(10000.0);
    double downH = area["dDownHeight"].toDouble(0.0);
    QJsonArray points = area["vecPoint"].toArray();

    if (points.isEmpty()) return QString();

    ts << "# 区域: " << message << "\n";

    if (areaType == 1) {
        // 圆形区域 — AFSIM 用 cylindrical zone
        if (points.size() >= 1) {
            QJsonObject center = points[0].toObject();
            double cLon = center["x"].toDouble(0.0);
            double cLat = center["y"].toDouble(0.0);
            double radius = 10000.0; // 默认 10km
            if (points.size() >= 2) {
                QJsonObject edge = points[1].toObject();
                double eLon = edge["x"].toDouble(0.0);
                double eLat = edge["y"].toDouble(0.0);
                double dLat = (eLat - cLat) * METERS_PER_DEG;
                double dLon = (eLon - cLon) * METERS_PER_DEG * cos(cLat * DEG_TO_RAD);
                radius = sqrt(dLat * dLat + dLon * dLon);
            }
            ts << "zone " << name << "\n";
            ts << "   cylindrical lat_lon\n";
            ts << "      center " << QString::number(qFabs(cLat), 'f', 6)
               << (cLat >= 0 ? "n " : "s ")
               << QString::number(qFabs(cLon), 'f', 6)
               << (cLon >= 0 ? "e " : "w ") << "\n";
            ts << "      radius " << QString::number(radius, 'f', 1) << " m\n";
            ts << "   minimum_altitude " << QString::number(downH, 'f', 1) << " m\n";
            ts << "   maximum_altitude " << QString::number(upH, 'f', 1) << " m\n";
            ts << "end_zone\n\n";
        }
    } else {
        // 多边形区域
        ts << "zone " << name << "\n";
        ts << "   polygonal lat_lon\n";
        for (const QJsonValue& pv : points) {
            QJsonObject pt = pv.toObject();
            double pLon = pt["x"].toDouble(0.0);
            double pLat = pt["y"].toDouble(0.0);
            ts << "      point "
               << QString::number(qFabs(pLat), 'f', 6)
               << (pLat >= 0 ? "n " : "s ")
               << QString::number(qFabs(pLon), 'f', 6)
               << (pLon >= 0 ? "e" : "w") << "\n";
        }
        ts << "   minimum_altitude " << QString::number(downH, 'f', 1) << " m\n";
        ts << "   maximum_altitude " << QString::number(upH, 'f', 1) << " m\n";
        ts << "end_zone\n\n";
    }

    ts.flush();
    return out;
}

// ============================================================================
// 编队成员绝对位置计算
// ============================================================================

void ScenarioConverter::calcMemberPosition(double leaderLon, double leaderLat, double leaderAlt,
                                           double leaderHdg,
                                           double relDis, double relHdg, double relAlt,
                                           double& outLon, double& outLat, double& outAlt)
{
    double totalHdgRad = (leaderHdg + relHdg) * DEG_TO_RAD;
    double deltaLat = -(relDis * cos(totalHdgRad)) / METERS_PER_DEG;
    double deltaLon = (relDis * sin(totalHdgRad)) / (METERS_PER_DEG * cos(leaderLat * DEG_TO_RAD));

    outLat = leaderLat + deltaLat;
    outLon = leaderLon + deltaLon;
    outAlt = leaderAlt + relAlt;
}
