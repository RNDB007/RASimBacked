// 校验
// ============================================================================

bool ScenarioConverter::validateScenario(const QJsonObject& json, QString& errorMsg)
{
    if (json.isEmpty()) {
        errorMsg = "scenario object is empty";
        return false;
    }
    if (!json.contains("vecTeam")) {
        errorMsg = "missing required field: vecTeam";
        return false;
    }
    const QJsonArray teams = json["vecTeam"].toArray();
    if (teams.isEmpty()) {
        errorMsg = "vecTeam is empty, at least one team required";
        return false;
    }
    for (int i = 0; i < teams.size(); ++i) {
        QJsonObject team = teams[i].toObject();
        QJsonObject manner = team["tObjManner"].toObject();
        if (manner.isEmpty()) {
            errorMsg = QString("team[%1] missing tObjManner").arg(i);
            return false;
        }
        QJsonObject status = manner["iObjStatus"].toObject();
        if (status.isEmpty()) {
            errorMsg = QString("team[%1].tObjManner missing iObjStatus").arg(i);
            return false;
        }
        QJsonObject pos = status["iPos"].toObject();
        if (pos.isEmpty()) {
            errorMsg = QString("team[%1].tObjManner.iObjStatus missing iPos").arg(i);
            return false;
        }
    }
    return true;
}

